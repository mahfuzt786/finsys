    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
         Report r=new Report();
        Date tx = txtstartdate.getDate();  
        Date end=txtenddate.getDate();
        int status=0;
        DateFormat oDateFormat = new SimpleDateFormat("dd-MM-yyyy");
      
        String tax_date = oDateFormat.format(tx);
        System.out.println("Formated :"+tax_date);
        String startdate=oDateFormat.format(tx);
        String enddate=oDateFormat.format(end);
        try {
            if(issueradio.isSelected()){
                status=1;
            }else  if(ledgerradio.isSelected()){
                status=2;
            }else  if(storeradio.isSelected()){
                status=3;
            }else  if(itemwiseradio.isSelected()){
                status=4;
            }else  if(stockinradio.isSelected()){
                status=5;
            }else  if(essradio.isSelected()){
                status=6;
            }
            r.filter_report(startdate,enddate,status);
        } catch (ParseException|JRException|IOException  ex) {
            Logger.getLogger(ReportF.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_btnaddActionPerformed

